    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        String maNH = txtMaNhaHang.getText();
        String tenNH = txtTenNhaHang.getText();
        String diachi = txtDiaChi.getText();
        String website = txtWebsite.getText();
        String soDT = txtSoDienThoai.getText();
        int chiphi = Integer.parseInt(txtChiPhi.getText());
        
        NhaHangDTO nhDTO = new NhaHangDTO(maNH, tenNH, diachi, website, soDT, chiphi);
        NhaHangBUS.CapNhatTTNH(nhDTO);
        
        int i = tblNhaHang.getSelectedRow();
        NhaHangBUS.xemDSNH();
        tableModel.setValueAt(maNH, i, 0);
        tableModel.setValueAt(tenNH, i, 1);
        tableModel.setValueAt(diachi, i, 2);
        tableModel.setValueAt(website, i, 3);
        tableModel.setValueAt(soDT, i, 4);
        tableModel.setValueAt(chiphi, i, 5);
        tblNhaHang.setModel(tableModel);
    }//GEN-LAST:event_btnCapNhatActionPerformed

