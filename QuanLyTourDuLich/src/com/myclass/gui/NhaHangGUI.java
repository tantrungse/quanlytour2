    private void showNhaHang(){
        nhahangList = NhaHangBUS.xemDSNH();
        
        tableModel.setRowCount(0);
        
        nhahangList.forEach(nhDTO -> {
            tableModel.addRow(new Object[]{
                nhDTO.getMaNH(), nhDTO.getTenNH(), nhDTO.getDiachi(), nhDTO.getWebsite(), nhDTO.getSoDT(), nhDTO.getChiphi()
            });
        });
    }
    
    private void showData(){
        int selectedIndex = tblNhaHang.getSelectedRow();
        
        if(selectedIndex >= 0){
            NhaHangDTO nhDTO = nhahangList.get(selectedIndex);
            
            txtMaNhaHang.setText(nhDTO.getMaNH());
            txtTenNhaHang.setText(nhDTO.getTenNH());
            txtDiaChi.setText(nhDTO.getDiachi());
            txtWebsite.setText(nhDTO.getWebsite());
            txtSoDienThoai.setText(nhDTO.getSoDT());
            txtChiPhi.setText(Integer.toString(nhDTO.getChiphi()));
            
        }
    }

